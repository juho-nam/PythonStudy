# for

for waiting_no in range(5): # 0 에서 5 미만까지
    print("대기번호 : {0}".format(waiting_no))
# 대기번호 : 0
# 대기번호 : 1
# 대기번호 : 2
# 대기번호 : 3
# 대기번호 : 4

starbucks = ["아이언맨", "토르", "그루트"]
for customer in starbucks:
    print("{0}, 커피가 준비되었습니다.".format(customer))
# 아이언맨, 커피가 준비되었습니다.
# 토르, 커피가 준비되었습니다.
# 그루트, 커피가 준비되었습니다.